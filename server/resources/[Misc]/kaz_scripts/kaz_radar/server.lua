local Tunnel = module("vrp", "lib/Tunnel")
local Proxy = module("vrp", "lib/Proxy")

vRP = Proxy.getInterface("vRP")
vRPclient = Tunnel.getInterface("vRP", "kaz_scripts")

RegisterServerEvent('kaz_radar:tjekperm')
AddEventHandler('kaz_radar:tjekperm', function()
local source = source
local user_id = vRP.getUserId({source})
	if vRP.hasGroup({user_id,"Politi-Job"}) then
	state = "sandt"
	TriggerClientEvent('kaz_radar:godkendt', source, state)
	else
	state = "falsk"
	TriggerClientEvent('kaz_radar:godkendt', source, state)
	end
end)