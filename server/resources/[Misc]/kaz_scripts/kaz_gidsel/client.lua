function takeHostage()
	ClearPedSecondaryTask(GetPlayerPed(-1))
	DetachEntity(GetPlayerPed(-1), true, false)
	for i=1, #hostageAllowedWeapons do
		if HasPedGotWeapon(GetPlayerPed(-1), GetHashKey(hostageAllowedWeapons[i]), false) then
				canTakeHostage = true 
				foundWeapon = GetHashKey(hostageAllowedWeapons[i])
				break					
		end
	end

	if not canTakeHostage then 
		TriggerEvent("pNotify:SendNotification",{text = "Du skal bruge et gyldigt våben!",type = "success",timeout = (8500),layout = "centerRight",queue = "global",animation = {open = "gta_effects_fade_in", close = "gta_effects_fade_out"}})
	end

	if not holdingHostageInProgress and canTakeHostage then		
		local player = PlayerPedId()	
		lib = 'anim@gangops@hostage@'
		anim1 = 'perp_idle'
		lib2 = 'anim@gangops@hostage@'
		anim2 = 'victim_idle'
		distans = 0.11 -- Higher = closer to camera
		distans2 = -0.24 -- higher = left
		height = 0.0
		spin = 0.0		
		length = 100000
		controlFlagMe = 49
		controlFlagTarget = 49
		animFlagTarget = 50
		attachFlag = true 
		local closestPlayer = GetClosestPlayer(2)
		target = GetPlayerServerId(closestPlayer)
		nplayer = GetClosestPlayer2(2)
		incartarget = IsPedInAnyVehicle(nplayer, true)
		incarplayer = IsPedInAnyVehicle(player, true)
		local health = GetEntityHealth(nplayer)
		if IsPedArmed(player, 7) ~= false then 
			if health >= 106 then
				if incarplayer ~= 1 then 
					if closestPlayer ~= nil then
						if incartarget ~= 1 then 
							SetCurrentPedWeapon(GetPlayerPed(-1), foundWeapon, true)
							holdingHostageInProgress = true
							holdingHostage = true 
							TriggerServerEvent('kaz_gidsel:sync', closestPlayer, lib,lib2, anim1, anim2, distans, distans2, height,target,length,spin,controlFlagMe,controlFlagTarget,animFlagTarget,attachFlag)
						else
							TriggerEvent("pNotify:SendNotification",{text = "Personen sidder i et køretøj!",type = "success",timeout = (8500),layout = "centerRight",queue = "global",animation = {open = "gta_effects_fade_in", close = "gta_effects_fade_out"}})
						end
					else
						TriggerEvent("pNotify:SendNotification",{text = "Ingen i nærheden der kan tages som gidsel!",type = "success",timeout = (8500),layout = "centerRight",queue = "global",animation = {open = "gta_effects_fade_in", close = "gta_effects_fade_out"}})
						
					end 
				end
			else
				TriggerEvent("pNotify:SendNotification",{text = "Du kan ikke tage en død som gidsel.",type = "success",timeout = (8500),layout = "centerRight",queue = "global",animation = {open = "gta_effects_fade_in", close = "gta_effects_fade_out"}})
			end
		else
			TriggerEvent("pNotify:SendNotification",{text = "Du skal have et våben fremme.",type = "success",timeout = (8500),layout = "centerRight",queue = "global",animation = {open = "gta_effects_fade_in", close = "gta_effects_fade_out"}})
		end
	end
	canTakeHostage = false 
end 

RegisterNetEvent('kaz_gidsel:syncTarget')
AddEventHandler('kaz_gidsel:syncTarget', function(target, animationLib, animation2, distans, distans2, height, length,spin,controlFlag,animFlagTarget,attach)
	local playerPed = GetPlayerPed(-1)
	local targetPed = GetPlayerPed(GetPlayerFromServerId(target))
	if holdingHostageInProgress then 
		holdingHostageInProgress = false 
	else 
		holdingHostageInProgress = true
	end
	beingHeldHostage = true 
	RequestAnimDict(animationLib)

	while not HasAnimDictLoaded(animationLib) do
		Citizen.Wait(10)
	end
	if spin == nil then spin = 180.0 end
	if attach then 
		AttachEntityToEntity(GetPlayerPed(-1), targetPed, 0, distans2, distans, height, 0.5, 0.5, spin, false, false, false, false, 2, false)
	end
	
	if controlFlag == nil then controlFlag = 0 end
	
	if animation2 == "victim_fail" then 
		SetEntityHealth(GetPlayerPed(-1),0)
		TaskPlayAnim(playerPed, animationLib, animation2, 8.0, -8.0, length, controlFlag, 0, false, false, false)
		beingHeldHostage = false 
		holdingHostageInProgress = false 
	elseif animation2 == "shoved_back" then 
		holdingHostageInProgress = false 
		TaskPlayAnim(playerPed, animationLib, animation2, 8.0, -8.0, length, controlFlag, 0, false, false, false)
		beingHeldHostage = false 
	else
		TaskPlayAnim(playerPed, animationLib, animation2, 8.0, -8.0, length, controlFlag, 0, false, false, false)	
	end
end)

