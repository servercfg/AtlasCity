AddEventHandler('nicki_armoryv2:checkPerms', function()
		local user_id = vRP.getUserId({source})
    if user_id then
        if vRP.hasPermission({user_id, "police.pc"}) then
            TriggerClientEvent('nicki_armoryv2:OpenArmory', source)
        else
            TriggerClientEvent('mythic_notify:client:SendAlert', source, { type = 'error', text = 'Du har ikke adgang til VÃ¥benskabet'}, 5000)
        end
    end
end)